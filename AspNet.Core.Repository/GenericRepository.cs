        public virtual void Update(TEntity entity, params object[] avoidProperties)
        {
            if (avoidProperties.Length > 0)
            {
                AvoidProperty(entity, avoidProperties.ToList());
            }
            else
            {
                _dbSet.Update(entity);
            }
        }


