        /// <summary>
        /// Updates the specified entities.
        /// </summary>
        /// <param name="entities">The entities.</param>
        public virtual void Update(IEnumerable<TEntity> entities, params object[] avoidProperties)
        {
            foreach (TEntity entity in entities)
            {
                if (avoidProperties.Length > 0)
                {
                    AvoidProperty(entity, avoidProperties.ToList());
                }
                else
                {
                    _dbSet.Update(entity);
                }
            }
        }

        /// <summary>
        /// Avoid these property which are not required for update
        /// </summary>
        /// <param name="entity"></param>
        /// <param name="avoidProperties"></param>
        /// <returns></returns>
        private TEntity AvoidProperty(TEntity entity, List<object> avoidProperties)
        {
            EntityEntry entry = _dbSet.Update(entity);

            if (avoidProperties != null)
            {
                foreach (var item in avoidProperties)
                {
                    foreach (PropertyInfo p in item.GetType().GetProperties())
                        entry.Property(p.Name).IsModified = false;
                }
            }

            return entity;
        }

        /// <summary>
        /// Avoid these property which are not required for update
        /// </summary>
        /// <param name="entity"></param>
        /// <param name="avoidProperties"></param>
        /// <returns></returns>
        private async Task<TEntity> AvoidPropertyAsync(TEntity entity, List<object> avoidProperties)
        {
            EntityEntry entry = _dbSet.Update(entity);

            if (avoidProperties != null)
            {
                foreach (var item in avoidProperties)
                {
                    foreach (PropertyInfo p in item.GetType().GetProperties())
                        entry.Property(p.Name).IsModified = false;
                }
            }

            return await Task.FromResult<TEntity>(entity);
        }
